import platform
import getpass
from termcolor import cprint

def print_with_art(art, text, art_color = None):
    text_lines = text.splitlines()
    for idx, line in enumerate(art.splitlines()):
        cprint(line, art_color, end = " ")
        try:
            cprint(text_lines[idx])
        except IndexError:
            print()

